/*
  Data Types Intro
  - String
  - Number
  - Array => Object
  - Object
  - Boolean
  - Undefined
*/

console.log(typeof "Haitham"); // String
console.log(typeof 5000); // Number
console.log(typeof 5000.99); // Number
console.log(typeof [10, 15, 17]); // Object
console.log(typeof ["Haitham", "Mohammed", "Hasan"]); // Object
console.log(typeof { name: 'Haitham', age: 21, country: 'Ps' }); // Object
console.log(typeof true); // Boolean
console.log(typeof false); // Boolean
console.log(typeof undefined); // Undefined
console.log(typeof null); // Object (This is a bug in JavaScript, null should be of type null, but it is of type object)