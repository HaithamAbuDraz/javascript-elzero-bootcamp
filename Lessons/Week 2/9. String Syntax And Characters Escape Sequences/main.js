/*
  String Syntax + Character Escape Sequences
  \ Escape + Line Continu
  \n New Line
*/

console.log("Haitham \"Abu Draz\""); // Haitham "Abu Draz"
console.log("Haitham \'Abu Draz\'"); // Haitham 'Abu Draz'
console.log("Haitham Abu Draz"); // Haitham Abu Draz
console.log("Haitham \\Abu Draz"); // Haitham \Abu Draz
console.log("Haitham \
Abu \
Draz"); // Haitham Abu Draz
console.log("Haitham \nAbu \nDraz");
// Haitham
// Abu
// Draz